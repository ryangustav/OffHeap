const { LRUCache } = require('lru-cache');
const { PerformanceObserver } = require('perf_hooks');
const { CacheManager } = require('../index.js');

function formatMemory(bytes) {
  return (bytes / 1024 / 1024).toFixed(2) + ' MB';
}

// Generate unique payloads to populate the caches
function getLargePayload(id) {
  return {
    id,
    title: 'Product Details Model #' + id,
    price: Math.random() * 500,
    active: true,
    tags: ['electronics', 'retail', 'promo', 'imported'],
    description: 'a'.repeat(400) // ~500 bytes total payload
  };
}

// Simulate transient server memory allocations (garbage generated by active requests)
function allocateRequestGarbage() {
  const list = [];
  for (let i = 0; i < 300; i++) {
    list.push({
      requestId: Math.random(),
      route: '/api/v1/products/' + Math.floor(Math.random() * 1000),
      timestamp: Date.now(),
      meta: { ua: 'Mozilla/5.0', ip: '127.0.0.1', method: 'GET' }
    });
  }
}

async function runGCPressureBenchmark() {
  console.log('Populating 500,000 unique keys to simulate a production-sized cache...');
  const numKeys = 500000;
  const keys = Array.from({ length: numKeys }, (_, i) => `key-${i}`);

  const opsCount = 1000000;
  const garbageFrequency = 200; // Allocate garbage every 200 operations
  const gcForceFrequency = 10000; // Force GC every 10k operations

  // Trace V8 GC activity using PerformanceObserver
  let gcCount = 0;
  let totalGcTime = 0;
  let maxGcTime = 0;

  const obs = new PerformanceObserver((list) => {
    for (const entry of list.getEntries()) {
      gcCount++;
      totalGcTime += entry.duration;
      if (entry.duration > maxGcTime) {
        maxGcTime = entry.duration;
      }
    }
  });

  // 1. Benchmark Pure JS lru-cache under GC Pressure
  global.gc && global.gc();
  const startHeapLru = process.memoryUsage().heapUsed;
  const startRssLru = process.memoryUsage().rss;

  const jsLru = new LRUCache({ max: numKeys });
  console.log('Filling JS lru-cache...');
  for (let i = 0; i < numKeys; i++) {
    jsLru.set(keys[i], getLargePayload(i));
  }
  console.log(`JS lru-cache Heap size: ${formatMemory(process.memoryUsage().heapUsed - startHeapLru)}`);

  obs.observe({ entryTypes: ['gc'] });
  gcCount = 0;
  totalGcTime = 0;
  maxGcTime = 0;

  console.log('\nRunning lru-cache GC Pressure Test...');
  const startLru = Date.now();
  const lruLatencies = new Float64Array(opsCount);

  for (let i = 0; i < opsCount; i++) {
    const key = keys[Math.floor(Math.random() * numKeys)];
    const opStart = performance.now();
    
    // 90% reads / 10% writes
    if (i % 10 === 0) {
      jsLru.set(key, getLargePayload(i));
    } else {
      jsLru.get(key);
    }
    
    lruLatencies[i] = performance.now() - opStart;

    if (i % garbageFrequency === 0) {
      allocateRequestGarbage();
    }

    if (i % gcForceFrequency === 0 && global.gc) {
      global.gc(); // Force V8 to run a GC sweep to check the active heap
    }
  }

  const durationLru = Date.now() - startLru;
  
  // Wait to flush PerformanceObserver events
  await new Promise((resolve) => setTimeout(resolve, 300));
  
  const endHeapLru = process.memoryUsage().heapUsed;
  const endRssLru = process.memoryUsage().rss;
  
  const lruGcCount = gcCount;
  const lruGcTime = totalGcTime;
  const lruMaxGcTime = maxGcTime;

  obs.disconnect();
  jsLru.clear();
  global.gc && global.gc();

  // 2. Benchmark OffHeap Hybrid under GC Pressure
  global.gc && global.gc();
  const startHeapOffheap = process.memoryUsage().heapUsed;
  const startRssOffheap = process.memoryUsage().rss;

  const manager = new CacheManager();
  // L2 capacity 500k, L1 capacity 10k (only 2% JS memory footprint!)
  const offheap = manager.createCache('zipf-hybrid-gc', {
    policy: 'lru',
    capacity: numKeys,
    shards: 16,
    l1Capacity: 10000
  });

  console.log('\nFilling OffHeap L1/L2 Cache...');
  for (let i = 0; i < numKeys; i++) {
    offheap.set(keys[i], getLargePayload(i));
  }
  console.log(`OffHeap JS Heap size: ${formatMemory(process.memoryUsage().heapUsed - startHeapOffheap)}`);

  // Reset metrics
  gcCount = 0;
  totalGcTime = 0;
  maxGcTime = 0;
  obs.observe({ entryTypes: ['gc'] });

  console.log('Running OffHeap GC Pressure Test...');
  const startOffheap = Date.now();
  const offheapLatencies = new Float64Array(opsCount);

  for (let i = 0; i < opsCount; i++) {
    const key = keys[Math.floor(Math.random() * numKeys)];
    const opStart = performance.now();
    
    if (i % 10 === 0) {
      offheap.set(key, getLargePayload(i));
    } else {
      offheap.get(key);
    }
    
    offheapLatencies[i] = performance.now() - opStart;

    if (i % garbageFrequency === 0) {
      allocateRequestGarbage();
    }

    if (i % gcForceFrequency === 0 && global.gc) {
      global.gc(); // Force V8 to run a GC sweep to check the active heap
    }
  }

  const durationOffheap = Date.now() - startOffheap;
  
  // Wait to flush PerformanceObserver events
  await new Promise((resolve) => setTimeout(resolve, 300));
  
  const endHeapOffheap = process.memoryUsage().heapUsed;
  const endRssOffheap = process.memoryUsage().rss;

  const offheapGcCount = gcCount;
  const offheapGcTime = totalGcTime;
  const offheapMaxGcTime = maxGcTime;

  obs.disconnect();
  offheap.dispose();
  manager.dispose();
  global.gc && global.gc();

  // Sort latencies for percentiles
  lruLatencies.sort();
  offheapLatencies.sort();

  const getPercentile = (arr, pct) => {
    const idx = Math.floor(arr.length * (pct / 100));
    return (arr[idx] * 1000).toFixed(1) + ' μs';
  };

  const getAverage = (arr) => {
    let sum = 0;
    for (let i = 0; i < arr.length; i++) {
      sum += arr[i];
    }
    return ((sum / arr.length) * 1000).toFixed(1) + ' μs';
  };

  console.log('\n========================================================================');
  console.log('🏆 GARBAGE COLLECTION LATENCY PRESSURE TEST (500k Active Keys, 1M Ops)');
  console.log('========================================================================');
  console.log('| Metric              | JS lru-cache (In-Heap) | OffHeap Hybrid (L1+L2)   |');
  console.log('|---------------------|------------------------|--------------------------|');
  console.log(`| Total Time          | ${durationLru} ms               | ${durationOffheap} ms                 |`);
  console.log(`| Average Cache Lat   | ${getAverage(lruLatencies).padEnd(22)} | ${getAverage(offheapLatencies).padEnd(24)} |`);
  console.log(`| p50 Cache Latency   | ${getPercentile(lruLatencies, 50).padEnd(22)} | ${getPercentile(offheapLatencies, 50).padEnd(24)} |`);
  console.log(`| p90 Cache Latency   | ${getPercentile(lruLatencies, 90).padEnd(22)} | ${getPercentile(offheapLatencies, 90).padEnd(24)} |`);
  console.log(`| p99 Cache Latency   | ${getPercentile(lruLatencies, 99).padEnd(22)} | ${getPercentile(offheapLatencies, 99).padEnd(24)} |`);
  console.log(`| p99.9 Cache Latency | ${getPercentile(lruLatencies, 99.9).padEnd(22)} | ${getPercentile(offheapLatencies, 99.9).padEnd(24)} |`);
  console.log('|---------------------|------------------------|--------------------------|');
  console.log(`| V8 GC Events Trigger| ${lruGcCount.toString().padEnd(22)} | ${offheapGcCount.toString().padEnd(24)} |`);
  console.log(`| Total V8 GC Duration| ${lruGcTime.toFixed(1).padEnd(19)} ms | ${offheapGcTime.toFixed(1).padEnd(21)} ms |`);
  console.log(`| Worst Single GC Stop| ${lruMaxGcTime.toFixed(1).padEnd(19)} ms | ${offheapMaxGcTime.toFixed(1).padEnd(21)} ms |`);
  console.log(`| Heap Usage (End)    | ${formatMemory(endHeapLru).padEnd(22)} | ${formatMemory(endHeapOffheap).padEnd(24)} |`);
  console.log(`| RSS Memory (End)    | ${formatMemory(endRssLru).padEnd(22)} | ${formatMemory(endRssOffheap).padEnd(24)} |`);
  console.log('========================================================================');
}

runGCPressureBenchmark().catch(console.error);
