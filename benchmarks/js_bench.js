const { Bench } = require('tinybench');
const { LRUCache } = require('lru-cache');
const { PerformanceObserver } = require('perf_hooks');
const { CacheManager } = require('../index.js');

function formatMemory(bytes) {
  return (bytes / 1024 / 1024).toFixed(2) + ' MB';
}

// Generate unique payloads to populate the caches
function getLargePayload(id) {
  return {
    id,
    title: 'Product Details Model #' + id,
    price: Math.random() * 500,
    active: true,
    tags: ['electronics', 'retail', 'promo', 'imported'],
    description: 'a'.repeat(400) // ~500 bytes total payload
  };
}

// Simulate transient server memory allocations (garbage generated by active requests)
function allocateRequestGarbage() {
  const list = [];
  for (let i = 0; i < 300; i++) {
    list.push({
      requestId: Math.random(),
      route: '/api/v1/products/' + Math.floor(Math.random() * 1000),
      meta: { ua: 'Mozilla/5.0', ip: '127.0.0.1', method: 'GET' }
    });
  }
}

async function runMicroBenchmarks() {
  const manager = new CacheManager();
  const capacity = 10000;

  const lruCacheL1 = manager.createCache('bench-lru-l1', { policy: 'lru', capacity, l1Capacity: capacity });
  const lruCacheL2 = manager.createCache('bench-lru-l2', { policy: 'lru', capacity, l1Capacity: 0 });
  const arcCache = manager.createCache('bench-arc', { policy: 'arc', capacity, l1Capacity: 0 });
  const tinyLfuCache = manager.createCache('bench-tinylfu', { policy: 'tinylfu', capacity, l1Capacity: 0 });
  const jsLru = new LRUCache({ max: capacity });
  const jsMap = new Map();

  const keys = Array.from({ length: 20000 }, (_, i) => `key-${i}`);
  const val = { data: 'a'.repeat(100) };
  let writeIdx = 0;
  let readIdx = 0;

  const benchSet = new Bench({ time: 1000 });
  benchSet
    .add('Pure JS Map - Set', () => {
      jsMap.set(keys[writeIdx % keys.length], val);
      writeIdx++;
    })
    .add('JS lru-cache - Set', () => {
      jsLru.set(keys[writeIdx % keys.length], val);
      writeIdx++;
    })
    .add('OffHeap L1+L2 - Set', () => {
      lruCacheL1.set(keys[writeIdx % keys.length], val);
      writeIdx++;
    })
    .add('OffHeap L2 Only - Set', () => {
      lruCacheL2.set(keys[writeIdx % keys.length], val);
      writeIdx++;
    });

  console.log('=== SECTION 1: Micro-Benchmarking SET operations (10k capacity, 20k keys) ===');
  await benchSet.run();
  console.table(benchSet.table());

  for (let i = 0; i < capacity; i++) {
    jsMap.set(keys[i], val);
    jsLru.set(keys[i], val);
    lruCacheL1.set(keys[i], val);
    lruCacheL2.set(keys[i], val);
    arcCache.set(keys[i], val);
    tinyLfuCache.set(keys[i], val);
  }

  const benchGet = new Bench({ time: 1000 });
  benchGet
    .add('Pure JS Map - Get', () => {
      jsMap.get(keys[readIdx % capacity]);
      readIdx++;
    })
    .add('JS lru-cache - Get', () => {
      jsLru.get(keys[readIdx % capacity]);
      readIdx++;
    })
    .add('OffHeap L1 Hit - Get', () => {
      lruCacheL1.get(keys[readIdx % capacity]);
      readIdx++;
    })
    .add('OffHeap L2 Hit (LRU) - Get', () => {
      lruCacheL2.get(keys[readIdx % capacity]);
      readIdx++;
    })
    .add('OffHeap L2 Hit (ARC) - Get', () => {
      arcCache.get(keys[readIdx % capacity]);
      readIdx++;
    })
    .add('OffHeap L2 Hit (W-TinyLFU) - Get', () => {
      tinyLfuCache.get(keys[readIdx % capacity]);
      readIdx++;
    });

  console.log('\n=== SECTION 2: Micro-Benchmarking GET operations (100% Cache Hits) ===');
  await benchGet.run();
  console.table(benchGet.table());

  lruCacheL1.dispose();
  lruCacheL2.dispose();
  arcCache.dispose();
  tinyLfuCache.dispose();
  manager.dispose();
}

async function runBatchBenchmark() {
  const manager = new CacheManager();
  const capacity = 100000;
  const jsLru = new LRUCache({ max: capacity });
  const offheap = manager.createCache('bench-batch', { policy: 'lru', capacity });

  const numKeys = 20000;
  const keys = Array.from({ length: numKeys }, (_, i) => `key-${i}`);
  const val = { val: 'test-data-payload' };

  for (let i = 0; i < numKeys; i++) {
    jsLru.set(keys[i], val);
    offheap.set(keys[i], val);
  }

  console.log('\n=== SECTION 3: BATCH OPERATION THROUGHPUT (Statistical Tinybench Suite) ===');

  // 1. Batch Size 100
  const bench100 = new Bench({ time: 1000 });
  const keys100 = keys.slice(0, 100);
  bench100
    .add('JS lru-cache - Batch 100 (Loop)', () => {
      for (let i = 0; i < 100; i++) jsLru.get(keys100[i]);
    })
    .add('OffHeap L2 - Batch 100 (Loop)', () => {
      for (let i = 0; i < 100; i++) offheap._native.get(keys100[i]);
    })
    .add('OffHeap mget - Batch 100 (Single FFI)', () => {
      offheap.mget(keys100);
    });

  console.log('\n--- Batch size: 100 keys ---');
  await bench100.run();
  console.table(bench100.table());

  // 2. Batch Size 1000
  const bench1000 = new Bench({ time: 1000 });
  const keys1000 = keys.slice(0, 1000);
  bench1000
    .add('JS lru-cache - Batch 1000 (Loop)', () => {
      for (let i = 0; i < 1000; i++) jsLru.get(keys1000[i]);
    })
    .add('OffHeap L2 - Batch 1000 (Loop)', () => {
      for (let i = 0; i < 1000; i++) offheap._native.get(keys1000[i]);
    })
    .add('OffHeap mget - Batch 1000 (Single FFI)', () => {
      offheap.mget(keys1000);
    });

  console.log('\n--- Batch size: 1000 keys ---');
  await bench1000.run();
  console.table(bench1000.table());

  offheap.dispose();
  manager.dispose();
}

async function runGCPressureBenchmark() {
  console.log('\n=== SECTION 4: GARBAGE COLLECTION LATENCY PRESSURE TEST (500k Keys, 1M Ops) ===');
  if (!global.gc) {
    console.warn('⚠️ Warning: Run with --expose-gc flag to include V8 GC latency metrics. Skipping GC pressure test.');
    return;
  }

  console.log('Populating 500,000 unique keys to simulate a production-sized cache...');
  const numKeys = 500000;
  const keys = Array.from({ length: numKeys }, (_, i) => `key-${i}`);
  const opsCount = 1000000;
  const garbageFrequency = 200;
  const gcForceFrequency = 10000;

  let gcCount = 0;
  let totalGcTime = 0;
  let maxGcTime = 0;

  const obs = new PerformanceObserver((list) => {
    for (const entry of list.getEntries()) {
      gcCount++;
      totalGcTime += entry.duration;
      if (entry.duration > maxGcTime) {
        maxGcTime = entry.duration;
      }
    }
  });

  // 1. Benchmark Pure JS lru-cache
  global.gc();
  const startHeapLru = process.memoryUsage().heapUsed;
  const jsLru = new LRUCache({ max: numKeys });
  
  console.log('Filling JS lru-cache...');
  for (let i = 0; i < numKeys; i++) {
    jsLru.set(keys[i], getLargePayload(i));
  }
  console.log(`JS lru-cache Heap size: ${formatMemory(process.memoryUsage().heapUsed - startHeapLru)}`);

  obs.observe({ entryTypes: ['gc'] });
  gcCount = 0;
  totalGcTime = 0;
  maxGcTime = 0;

  console.log('Running lru-cache GC Pressure Test (1,000,000 operations)...');
  const startLru = Date.now();
  const lruLatencies = new Float64Array(opsCount);

  for (let i = 0; i < opsCount; i++) {
    const key = keys[Math.floor(Math.random() * numKeys)];
    const opStart = performance.now();
    
    if (i % 10 === 0) {
      jsLru.set(key, getLargePayload(i));
    } else {
      jsLru.get(key);
    }
    
    lruLatencies[i] = performance.now() - opStart;

    if (i % garbageFrequency === 0) {
      allocateRequestGarbage();
    }

    if (i % gcForceFrequency === 0) {
      global.gc();
    }
  }

  const durationLru = Date.now() - startLru;
  await new Promise((resolve) => setTimeout(resolve, 300));
  
  const endHeapLru = process.memoryUsage().heapUsed;
  const endRssLru = process.memoryUsage().rss;
  
  const lruGcCount = gcCount;
  const lruGcTime = totalGcTime;
  const lruMaxGcTime = maxGcTime;

  obs.disconnect();
  jsLru.clear();
  global.gc();

  // 2. Benchmark OffHeap Hybrid
  global.gc();
  const startHeapOffheap = process.memoryUsage().heapUsed;
  const manager = new CacheManager();
  const offheap = manager.createCache('zipf-hybrid-gc', {
    policy: 'lru',
    capacity: numKeys,
    shards: 16,
    l1Capacity: 10000
  });

  console.log('\nFilling OffHeap L1/L2 Cache...');
  for (let i = 0; i < numKeys; i++) {
    offheap.set(keys[i], getLargePayload(i));
  }
  console.log(`OffHeap JS Heap size: ${formatMemory(process.memoryUsage().heapUsed - startHeapOffheap)}`);

  gcCount = 0;
  totalGcTime = 0;
  maxGcTime = 0;
  obs.observe({ entryTypes: ['gc'] });

  console.log('Running OffHeap GC Pressure Test (1,000,000 operations)...');
  const startOffheap = Date.now();
  const offheapLatencies = new Float64Array(opsCount);

  for (let i = 0; i < opsCount; i++) {
    const key = keys[Math.floor(Math.random() * numKeys)];
    const opStart = performance.now();
    
    if (i % 10 === 0) {
      offheap.set(key, getLargePayload(i));
    } else {
      offheap.get(key);
    }
    
    offheapLatencies[i] = performance.now() - opStart;

    if (i % garbageFrequency === 0) {
      allocateRequestGarbage();
    }

    if (i % gcForceFrequency === 0) {
      global.gc();
    }
  }

  const durationOffheap = Date.now() - startOffheap;
  await new Promise((resolve) => setTimeout(resolve, 300));
  
  const endHeapOffheap = process.memoryUsage().heapUsed;
  const endRssOffheap = process.memoryUsage().rss;

  const offheapGcCount = gcCount;
  const offheapGcTime = totalGcTime;
  const offheapMaxGcTime = maxGcTime;

  obs.disconnect();
  offheap.dispose();
  manager.dispose();
  global.gc();

  // Sort latencies
  lruLatencies.sort();
  offheapLatencies.sort();

  const getPercentile = (arr, pct) => {
    const idx = Math.floor(arr.length * (pct / 100));
    return (arr[idx] * 1000).toFixed(1) + ' μs';
  };

  const getAverage = (arr) => {
    let sum = 0;
    for (let i = 0; i < arr.length; i++) {
      sum += arr[i];
    }
    return ((sum / arr.length) * 1000).toFixed(1) + ' μs';
  };

  console.log('\n========================================================================');
  console.log('🏆 GARBAGE COLLECTION LATENCY PRESSURE TEST RESULTS');
  console.log('========================================================================');
  console.log('| Metric              | JS lru-cache (In-Heap) | OffHeap Hybrid (L1+L2)   |');
  console.log('|---------------------|------------------------|--------------------------|');
  console.log(`| Total Time          | ${durationLru} ms               | ${durationOffheap} ms                 |`);
  console.log(`| Average Cache Lat   | ${getAverage(lruLatencies).padEnd(22)} | ${getAverage(offheapLatencies).padEnd(24)} |`);
  console.log(`| p50 Cache Latency   | ${getPercentile(lruLatencies, 50).padEnd(22)} | ${getPercentile(offheapLatencies, 50).padEnd(24)} |`);
  console.log(`| p90 Cache Latency   | ${getPercentile(lruLatencies, 90).padEnd(22)} | ${getPercentile(offheapLatencies, 90).padEnd(24)} |`);
  console.log(`| p99 Cache Latency   | ${getPercentile(lruLatencies, 99).padEnd(22)} | ${getPercentile(offheapLatencies, 99).padEnd(24)} |`);
  console.log(`| p99.9 Cache Latency | ${getPercentile(lruLatencies, 99.9).padEnd(22)} | ${getPercentile(offheapLatencies, 99.9).padEnd(24)} |`);
  console.log('|---------------------|------------------------|--------------------------|');
  console.log(`| V8 GC Events Trigger| ${lruGcCount.toString().padEnd(22)} | ${offheapGcCount.toString().padEnd(24)} |`);
  console.log(`| Total V8 GC Duration| ${lruGcTime.toFixed(1).padEnd(19)} ms | ${offheapGcTime.toFixed(1).padEnd(21)} ms |`);
  console.log(`| Worst Single GC Stop| ${lruMaxGcTime.toFixed(1).padEnd(19)} ms | ${offheapMaxGcTime.toFixed(1).padEnd(21)} ms |`);
  console.log(`| Heap Usage (End)    | ${formatMemory(endHeapLru).padEnd(22)} | ${formatMemory(endHeapOffheap).padEnd(24)} |`);
  console.log(`| RSS Memory (End)    | ${formatMemory(endRssLru).padEnd(22)} | ${formatMemory(endRssOffheap).padEnd(24)} |`);
  console.log('========================================================================');
  
  console.log('\n💡 Note on RSS Memory (End):');
  console.log('Because these tests run sequentially in the same OS process, the RSS Memory reported for the');
  console.log('second test (OffHeap) includes the V8-allocated memory pages that V8 did not yet return to the OS.');
  console.log('In isolated processes, OffHeap’s total RSS footprint is comparable to or lower than JS lru-cache.');
}

async function main() {
  await runMicroBenchmarks();
  await runBatchBenchmark();
  await runGCPressureBenchmark();
}

main().catch(console.error);
